package com.example.diegopillajo_examen;

public class encuestaDat {
    private String informacion;
    private String mediop;
    private String satisfaccionf;
    private String Cod;

    public encuestaDat() {
    }

    public String getInformacion() {
        return informacion;
    }

    public void setInformacion(String informacion) {
        this.informacion = informacion;
    }

    public String getMediop() {
        return mediop;
    }

    public void setMediop(String mediop) {
        this.mediop = mediop;
    }

    public String getSatisfaccionf() {
        return satisfaccionf;
    }

    public void setSatisfaccionf(String satisfaccionf) {
        this.satisfaccionf = satisfaccionf;
    }

    public String getCod() {
        return Cod;
    }

    public void setCod(String cod) {
        Cod = cod;
    }
}
